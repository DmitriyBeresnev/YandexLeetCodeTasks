class Solution:
    def convertToStr(self, a: int, b: int) -> str:
        if a == b:
            return str(a)
        return str(a) + "->" + str(b)

    def summaryRanges(self, nums: List[int]) -> List[str]:
        n = len(nums)
        res = list()
        if n == 0:
            return res
        prev = nums[0]
        cur = nums[0]
        for i in range(1, n):
            if nums[i] - cur != 1:
                res.append(self.convertToStr(prev, cur))
                prev = nums[i]
            cur = nums[i]
        res.append(self.convertToStr(prev, cur))
        return res


