class Solution:
    def compress(self, chars: List[str]) -> int:
        res = ""
        curSymbol = chars[0]
        curFreq = 0
        for symbol in chars:
            if symbol != curSymbol:
                if curFreq > 1:
                    res += curSymbol + str(curFreq)
                else:
                    res += curSymbol
                curFreq = 0
                curSymbol = symbol
            curFreq += 1
        if curFreq > 1:
            res += curSymbol + str(curFreq)
        else:
            res += curSymbol
        chars.clear()
        chars.extend(list(res))
        return len(res)


